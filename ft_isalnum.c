#include "libft.h"

int ft_isalnum(char c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <='Z') || (c >= 'a' && c <= 'z'))
		return 0;
	else
		return 1;
}

#include <stdio.h>
int main()
{
	char c = '&';
	ft_isalnum(c);
}
